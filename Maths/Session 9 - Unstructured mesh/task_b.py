import numpy as np

def TrBaryc(r,f,rp):
    lam = np.ndarray(3)
    lam[0] = ((r[1][1]-r[2][1])*(rp[0]-r[2][0])+(r[2][0]-r[1][0])*(rp[1]-r[2][1])) / ((r[1][1]-r[2][1])*(r[0][0]-r[2][0])+(r[2][0]-r[1][0])*(r[0][1]-r[2][1]))
    lam[1] = ((r[2][1]-r[0][1])*(rp[0]-r[2][0])+(r[0][0]-r[2][0])*(rp[1]-r[2][1])) / ((r[1][1]-r[2][1])*(r[0][0]-r[2][0])+(r[2][0]-r[1][0])*(r[0][1]-r[2][1]))
    lam[2] = 1 - lam[0] - lam[1]
    fr = lam[0]* f[0]+lam[1]* f[1]+lam[2]* f[2]
    return fr

r = ((-1,-1),(1,3),(2,0.5))
f= (1,1.5,3)
rp= (1,0.5)

print(TrBaryc(r,f,rp))